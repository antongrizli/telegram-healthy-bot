def get_text(key: str, lang: str = "en", **kwargs) -> str:
    lang = lang if lang in LOCALES else "en"
    text = LOCALES[lang].get(key, LOCALES["en"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except Exception:
            return text
    return text

def get_all_translations(key: str) -> list[str]:
    """Returns a list of unique translated strings for the given key across all locales."""
    translations = set()
    for locale_dict in LOCALES.values():
        translations.add(locale_dict.get(key, LOCALES["en"].get(key, key)))
    return list(translations)
